void main() {
  int num1 = 20;
  int num2 = 42;

  int sum = num1 + num2;
  int reduce = num2 - num1;
  int multiply = num1 * num2;
  double divide = num2 / num1;
  int divided = num2 ~/ num1;
  int modulo = num2 % num1;
  int increment = ++num1;
  int decrement = --num2;

  print(sum);
  print(reduce);
  print(multiply);
  print(divide);
  print(divided);
  print(modulo);
  print(increment);
  print(decrement);
}

