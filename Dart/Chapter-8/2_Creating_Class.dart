class Main {
  var name = "Andrew"; // Class properties or class instant variable

  void doIt() //=> Class method/ function
  {
    print("Hello, my name is $name");
  }
}

class Do {
  var job = "Developer";

  void start() {
    print("My job is $job");
  }
}

class Get {
  var salary = 25000;

  void receive() {
    print("Salary is $salary");
  }
}

// Class properties or class instant variable + Class method => call Class member

