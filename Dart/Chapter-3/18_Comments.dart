void main() {
  // This is Single-line Comment.
  int age = 23; // Variable for age
  print(age);

  /* 
    This is multi_lines comment.
    Can write multi-lines here.
  */
}

/* 
    Types of comment :

    1.Single Line Comment
    Starts with //
    Only that line is ignored
    Used for short explanations

    2.Multi-lines Comment :
    Starts with /* and ends with */
    Multiple lines are ignored
    Used for longer explanations

    3.Documentation Comment :
    Starts with /// (triple slash)
    Used for API documentation
    Shows in IDE when hovering
    Supports markdown formatting
*/
