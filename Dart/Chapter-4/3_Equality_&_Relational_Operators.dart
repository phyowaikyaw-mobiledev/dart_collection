     >=           Greater or equal     Left ≥ Right?          5 >= 5 → true
     <=           Less or equal        Left ≤ Right?          5 <= 10 → true

  EQUALITY OPERATORS (Check Same/Different)

  Operator            Name              What it does              Example
    ==              Equal to           Are they same?           5 == 5 → true
    !=             Not Equal to       Are they different?       5 != 3 → true

    Main Points:

    -All return boolean (true/false)
    -Used in if statements, loops, conditions
    -Work with numbers, strings, boolean








